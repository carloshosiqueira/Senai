#include <stdio.h>
int main(){
	int cont = 0;
	printf("Contador = %d\n",cont);
	cont++;
	printf("Contador = %d\n",cont);
	cont = cont + 1;
	printf("Contador = %d\n",cont);
	cont = cont - 1;
	printf("Contador = %d\n",cont);
	cont--;
	printf("Contador = %d\n",cont);
	cont = cont +3;
	printf("Contador = %d\n",cont);
	cont += 3;
	printf("Contador = %d\n",cont);
	cont = cont -3;
	printf("Contador = %d\n",cont);
	cont -= 3;
	printf("Contador = %d\n",cont);
	return 0;
}
